<script lang="ts">
  import { locale, _ } from "svelte-i18n";

  function toggleLanguage(): void {
    $locale = $locale === "en" ? "ar" : "en";
  }
</script>

<label class="switch">
  <input
    type="checkbox"
    on:change={toggleLanguage}
    checked={$locale === "ar"}
  />
  <span class="slider">
    <span class="label">{$_("language")}</span>
  </span>
</label>

